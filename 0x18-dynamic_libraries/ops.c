#include <stdio.h>

/**
 * add -  add 2 nums
 * @a: first num
 * @b: second num
 * Return: result after operation
*/
int add(int a, int b)
{
	return (a + b);
}
/**
 * mul -  multiply 2 nums
 * @a: first num
 * @b: second num
 * Return: result after operation
*/
int mul(int a, int b)
{
	return (a * b);
}
/**
 * sub -  subtract 2 nums
 * @a: first num
 * @b: second num
 * Return: result after operation
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - divide 2 nums
 * @a: first num
 * @b: second num
 * Return: result after operation
*/
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod -  give modulus of 2 nums
 * @a: first num
 * @b: second num
 * Return: result after operation
*/
int mod(int a, int b)
{
	return (a % b);
}
